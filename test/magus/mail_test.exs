  # ---------------------------------------------------------------------------
  # send_support_request/2
  # ---------------------------------------------------------------------------

  describe "send_support_request/2" do
    @form_data %{name: "Alice", email: "alice@example.com", topic: "bug", message: "Help!"}

    test "sends EN confirmation echoing topic and message", %{en_user: user} do
      assert {:ok, _} = Mail.send_support_request(user, @form_data)

      # Confirmation to the user
      assert_email_sent(fn email ->
        assert [{"", to_email}] = email.to
        assert to_email == to_string(user.email)
        assert email.subject =~ "received your support request"
        assert email.html_body =~ "We received your request"
        assert email.html_body =~ "Alice"
        # Echoes topic + message
        assert email.html_body =~ "bug"
        assert email.html_body =~ "Help!"
        assert email.text_body =~ "bug"
        assert email.text_body =~ "Help!"
        refute_template_options(email)
      end)
    end

    test "sends DE confirmation", %{de_user: user} do
      assert {:ok, _} = Mail.send_support_request(user, @form_data)

      assert_email_sent(fn email ->
        assert [{"", to_email}] = email.to
        assert to_email == to_string(user.email)
        assert email.html_body =~ "Wir haben deine Anfrage erhalten"
      end)
    end

    test "sends notification to support@magus.digital with reply_to", %{en_user: user} do
      assert {:ok, _} = Mail.send_support_request(user, @form_data)

      assert_receive {:email, %{to: [{"Magus Support", "support@magus.digital"}]} = email}
      assert email.subject == "Support Request [bug] from Alice"
      assert email.reply_to == {"Alice", "alice@example.com"}
      assert email.text_body =~ "Help!"
      assert email.text_body =~ "alice@example.com"
      assert email.text_body =~ "bug"
    end

    test "sends notification with plain email sender", _context do
      assert {:ok, _} = Mail.send_support_request("guest@example.com", @form_data)

      assert_receive {:email, %{to: [{"Magus Support", "support@magus.digital"}]} = email}
      assert email.reply_to == {"Alice", "alice@example.com"}
    end
  end

  # ---------------------------------------------------------------------------
  # Footer + language fallback
  # ---------------------------------------------------------------------------

  describe "footer and language fallback" do
    test "footer includes product name, year, support and discord links", %{en_user: user} do
      assert {:ok, _} = Mail.send_welcome(user)

      assert_email_sent(fn email ->
        assert email.html_body =~ "Magus"
        assert email.html_body =~ to_string(Date.utc_today().year)
        assert email.html_body =~ "https://magus.digital/help"
        assert email.html_body =~ "https://discord.gg/6EfPDhmWRb"

        assert email.text_body =~ to_string(Date.utc_today().year)
        assert email.text_body =~ "https://magus.digital/help"
        assert email.text_body =~ "https://discord.gg/6EfPDhmWRb"
      end)
    end

    test "defaults to EN when language is nil" do
      assert {:ok, _} =
               Mail.send_welcome(%{
                 email: "test@example.com",
                 display_name: "NoLang",
                 language: nil
               })

      assert_email_sent(fn email ->
        # EN copy, not German
        assert email.html_body =~ "Welcome to Magus"
        assert email.html_body =~ "NoLang"
      end)
    end
  end
