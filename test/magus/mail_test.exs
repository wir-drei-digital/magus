defmodule Magus.MailTest do
  @moduledoc """
  Tests for `Magus.Mail` — the centralized module for sending transactional
  emails with in-repo rendered HTML + plain-text bodies.

  Covers:
  - All 8 public email functions
  - `send_mail_verification_to/3` for override email
  - Locale selection (DE vs EN copy)
  - html_body / text_body presence and content (action_url, name)
  - Subject presence and absence of legacy Postmark template provider options
  - Handling plain email string input (no user struct)
  - Handling nil display_name
  - Support request confirmation + notification
  """
  use Magus.ResourceCase, async: true

  import Swoosh.TestAssertions

  alias Magus.Mail

  # ---------------------------------------------------------------------------
  # Helpers
  # ---------------------------------------------------------------------------

  # Drain any emails sent during setup (e.g., user registration confirmation).
  defp drain_mailbox do
    receive do
      {:email, _} -> drain_mailbox()
    after
      0 -> :ok
    end
  end

  # Asserts the email has no legacy Postmark template provider options.
  # Returns `:ok` so it can be the final expression of an `assert_email_sent`
  # callback (a `refute` returns `false`, which the callback treats as failure).
  defp refute_template_options(email) do
    refute Map.has_key?(email.provider_options, :template_id)
    refute Map.has_key?(email.provider_options, :template_model)
    :ok
  end

  # ---------------------------------------------------------------------------
  # Setup
  # ---------------------------------------------------------------------------

  setup do
    en_user = generate(user(display_name: "Alice", language: :en))
    de_user = generate(user(display_name: "Bob", language: :de))

    # Drain confirmation emails generated by user registration
    drain_mailbox()

    %{en_user: en_user, de_user: de_user}
  end

  # ---------------------------------------------------------------------------
  # send_magic_link/2
  # ---------------------------------------------------------------------------

  describe "send_magic_link/2" do
    test "renders English body for EN user", %{en_user: user} do
      url = "https://magus.digital/magic_link/abc"
      assert {:ok, _} = Mail.send_magic_link(user, url)

      assert_email_sent(fn email ->
        assert [{"", to_email}] = email.to
        assert to_email == to_string(user.email)

        assert email.subject != nil and email.subject != ""
        assert email.subject =~ "sign-in link"

        assert email.html_body != nil
        assert email.text_body != nil

        # English phrase + CTA label
        assert email.html_body =~ "Sign in to Magus"
        assert email.text_body =~ "Sign in to Magus"

        # name and action_url present in both bodies
        assert email.html_body =~ "Alice"
        assert email.text_body =~ "Alice"
        assert email.html_body =~ url
        assert email.text_body =~ url

        refute_template_options(email)
      end)
    end

    test "renders German body for DE user", %{de_user: user} do
      url = "https://magus.digital/magic_link/xyz"
      assert {:ok, _} = Mail.send_magic_link(user, url)

      assert_email_sent(fn email ->
        assert email.subject =~ "Anmeldelink"

        # German phrase + informal CTA
        assert email.html_body =~ "Bei Magus anmelden"
        assert email.text_body =~ "Bei Magus anmelden"

        assert email.html_body =~ "Bob"
        assert email.html_body =~ url
        assert email.text_body =~ url

        refute_template_options(email)
      end)
    end

    test "handles plain email string (no user struct), empty name, EN" do
      url = "https://example.com/link"
      assert {:ok, _} = Mail.send_magic_link("new@example.com", url)

      assert_email_sent(fn email ->
        assert [{"", "new@example.com"}] = email.to

        # EN fallback copy
        assert email.html_body =~ "Sign in to Magus"
        # Empty name renders a clean greeting (no trailing name)
        assert email.html_body =~ "Hi,"
        assert email.text_body =~ "Hi,"
        assert email.html_body =~ url
      end)
    end

    test "handles user with nil display_name (renders empty name, no crash)" do
      user = generate(user(display_name: nil, language: :en))
      drain_mailbox()

      assert {:ok, _} = Mail.send_magic_link(user, "https://example.com/link")

      assert_email_sent(fn email ->
        # No leftover name fragment; greeting falls back cleanly
        assert email.html_body =~ "Hi,"
        assert email.text_body =~ "Hi,"
      end)
    end

    test "includes message_stream provider option", %{en_user: user} do
      assert {:ok, _} = Mail.send_magic_link(user, "https://example.com/link")

      assert_email_sent(fn email ->
        assert email.provider_options.message_stream == "outbound"
      end)
    end

    test "sends from Magus", %{en_user: user} do
      assert {:ok, _} = Mail.send_magic_link(user, "https://example.com/link")

      assert_email_sent(fn email ->
        assert email.from == {"Magus", "support@magus.digital"}
      end)
    end
  end

  # ---------------------------------------------------------------------------
  # send_mail_verification/2
