  end

  # ---------------------------------------------------------------------------
  # send_mail_verification_to/3
  # ---------------------------------------------------------------------------

  describe "send_mail_verification_to/3" do
    test "sends to override email but uses user's name and locale", %{de_user: user} do
      override_email = "new-email@example.com"
      url = "https://example.com/confirm/token"

      assert {:ok, _} = Mail.send_mail_verification_to(override_email, user, url)

      assert_email_sent(fn email ->
        assert [{"", "new-email@example.com"}] = email.to
        # German locale, user's name
        assert email.html_body =~ "E-Mail bestätigen"
        assert email.html_body =~ "Bob"
        assert email.html_body =~ url
        refute_template_options(email)
      end)
    end
  end

